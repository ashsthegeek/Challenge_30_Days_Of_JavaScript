// La boucle while ou (tant que) en JavaScript
/*
    La boucle while, comme la boucle for permet de parcourir des elements.
    While est utilisé la plupart du temps, l'orsqu'on ne connait pas avance
    le nombre de fois que va tournée la boucle.
*/

// EXEMPLE 1

let i = 0 // Initialisation
while (i <= 5) { // La condition
  console.log(i)
  i++ // l'incerémentation
}
// 0 1 2 3 4 5

/*
    Rq: dans la boucle while comme dans for, on retrouve les mêmes concept à savoir :
    - L'initialisation
    - La condition 
    - l'incrémentation / Décrementation
    C'est juste la façon d'écrire qui change mais c'est le même principe
*/