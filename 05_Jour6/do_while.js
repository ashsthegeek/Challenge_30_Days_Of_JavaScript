/*
    La boucle do while (faire tant que) est pareil que les autres boucles
    sauf que do while s'exécute une fois peut importe la condition
*/

//  EXEMPLE

let i = 0 // Initialisation
do {
  console.log(i)
  i++ // Incrémentation / Décrementation
} while (i <= 5) // Condition

// 0 1 2 3 4 5

