/*
    Les boucles permettent de répéter des actions simplement et rapidement.
*/

// Boucle for

// SYNTAXE
    /* 
    for(initialisation, condition, incrementation/decrementation) {
        // Code à exécuté
    }
    */

//EXEMPLE
for(let i = 0; i <= 5; i++) {
    console.log(i)
}
// 0, 1, 2, 3, 4, 5

for (let j = 5; i >= 0; j--) {
    console.log(j)
}
// 5, 4, 3, 2, 1, 0